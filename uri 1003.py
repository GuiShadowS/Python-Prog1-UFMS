# -*- coding: utf-8 -*-

'''
Esse programa vai receber dois valores inteiros 
fornecidos pelo usuário e após isso irá calcular 
a soma entre eles, após isso mostrar na tela 
a palavra "SOMA = " seguida do resultado da soma
'''
# Recebe a entrada de dois valores inteiros fornecidos
# pelo usuário e salva nas variáveis A e B
a = int(input())
b = int(input())

# Com os valores das variáveis, é feito a soma e
# guardado o resultado na variável "soma"
soma = a + b

# Mostra na tela o resultado da adição de a + b
# da seguinte forma "SOMA =" e o resultado após
print("SOMA =",soma)